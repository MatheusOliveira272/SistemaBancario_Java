    public void setClienteEdit(Cliente clienteEdit) {
        this.clienteEdit = clienteEdit;
        
         if(clienteEdit == null){
            txtField_Nome.setText(null);
            txtField_Cpf.setText(null);
            txtField_Telefone.setText(null);
            txtField_Email.setText(null);
            txtField_Endereco.setText(null);
            
        }else{
            txtField_Nome.setText(clienteEdit.getNome());
            txtField_Cpf.setText(clienteEdit.getCpf());
            txtField_Telefone.setText(clienteEdit.getTelefone());
            txtField_Email.setText(clienteEdit.getEmail());
            txtField_Endereco.setText(clienteEdit.getEndereco());
            
        }
    }

    
